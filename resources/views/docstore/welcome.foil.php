    <h3 class="tw-mb-8">Welcome to the Document Store!</h3>

    <p>
        <b>IXP Manager</b> has a document store allowing administrators to upload documents to be made generally available
        for specific user classes (public, customer user, customer admin, superadmin). The document store supports:
    </p>

    <ul>
        <li>Upload any file type.</li>
        <li>Edit uploaded files including name, description, minimum access privilege and replacing the file itself.</li>
        <li>For non-public documents, logging and reporting of downloads (total downloads and unique user downloads).</li>
        <li>Display of text (.txt) and display and parsing of Markdown (.md) files within IXP Manager.</li>
        <li>Directory hierarchy allowing the categorization of files.</li>
        <li>Each directory can have explanatory text.</li>
        <li>Deletion of files and recursive deletion of directories.</li>
        <li>Logging of destructive actions.</li>
        <li>Please note that all actions except for viewing and downloading files are restricted to super users.</li>
    </ul>

    <p>
        You the use <em>Create Directory</em> and the <em>Upload File</em> buttons on the top right to start populating the
        directory store.
    </p>

    <p>
        <b>For more information, <a target="_blank" href="https://docs.ixpmanager.org/features/docstore/">see the official document here</a>.</b>
    </p>
</div>