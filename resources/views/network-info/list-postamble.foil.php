    <h3>VLAN Network Information</h3>

    <p>
        Some functionality of <b>IXP Manager</b> requires network information for VLANs that represent peering LANs. An example of this is
        the <a href="https://docs.ixpmanager.org/features/ixf-export/">IX-F Member Export</a>. Please add the network address and network
        mask length for each of IPv4 and IPv6 for these VLANs using this page.
    </p>

    <p>
        Note this is not required for private VLANs.
    </p>

