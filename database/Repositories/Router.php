                        continue;
                    }
                }

                $tabs[ $r->getVlan()->getInfrastructure()->getName() ][ $r->getProtocol() ][] = [ 'handle' => $r->getHandle() , 'name' => $r->getName(), 'last-updated' => $r->getLastUpdated() ];

            }

        }

        foreach( $tabs as $infra => $protocol){
            ksort($tabs[ $infra ] );
        }

        return $tabs;
    }

