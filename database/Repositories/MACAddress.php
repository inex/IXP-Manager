                    s.name AS switchname, 
                    GROUP_CONCAT( sp.name ) AS switchport, 
                    GROUP_CONCAT( DISTINCT ipv4.address ) AS ip4, 
                    GROUP_CONCAT( DISTINCT ipv6.address ) AS ip6,
