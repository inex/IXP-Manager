    /**
     * Register the Horizon gate.
     *
     * This gate determines who can access Horizon in non-local environments.
     *
     * @return void
     */
    protected function gate(): void
    {
        Gate::define('viewHorizon', function ( $user ) {
            /** @var User $us */
            $us = Auth::getUser();
            return Auth::check() && $us->isSuperUser();
        });
